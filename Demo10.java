package pack;

public class Demo10 {

	public static void main(String[] args) {
		
		float a = 12.35f ;
		
		float b = 5.25f ;
		
		System.out.println(a); // Data is displayed in decimal.
		
		System.out.println(b); // Data is displayed in decimal.
		
		float c = a + b ; 
		
		System.out.println(c);
		
		float d = a - b ; 
		
		System.out.println(d);
		
		float e = a * b ; 
		
		System.out.println(e);
		
		float f = a / b ; 
		
		System.out.println(f);
		
		float g = a % b ; 
		
		System.out.println(g);
	}
}
