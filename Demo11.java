package pack;

public class Demo11 {

	public static void main(String[] args) {
		
		int a = 11 ;
		
		int b = 25 ;
		
		boolean c = a < b ; // 11 is less than 25, is true.
		
		System.out.println(c);
		
		boolean d = a > b ; // 11 is greater than 25, is false.
		
		System.out.println(d);
		
		boolean e = a <= b ; // 11 is less than or equal to 25, is true.
		
		System.out.println(e);
		
		boolean f = a >= b ; // 11 is greater than or equal to 25, is false.
		
		System.out.println(f);
		
		boolean g = a == b ; // 11 is equal to 25, is false.
		
		System.out.println(g);
		
		boolean h = a != b ; // 11 is not equal to 25 is true.
		
		System.out.println(h);
		
	}
}
