package pack;

public class Demo5 {

	public static void main(String[] args) {
		
		byte  w = 33 ; // byte can store value 33
		
		System.out.println(w);
		
		short a = 1245 ; // short can store value 1245
		
		System.out.println(a);
		
		int r = 43097 ; // int can store value 43097
		
		System.out.println(r);
		
		long b = 456712345 ; // long can store value 456712345.
		
		System.out.println(b);
	}
}
