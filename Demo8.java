package pack;

public class Demo8 {

	public static void main(String[] args) {
		
		char a = 'V' ; // data accepted as it is an upper case letter.
		
		char b = 'r' ; // data accepted as it is an lower case letter.
		
		char c = '^' ; // data accepted as it is a special symbol.
		
		char d = '9' ; // data accepted as it is also a character.
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		
		boolean e = true ; // data accepted as it is a boolean value.
		
		boolean f = false ; // data accepted as it is a boolean value.
		
		System.out.println(e);
		System.out.println(f);
	}
}
