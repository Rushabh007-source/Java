package pack;

public class Demo9 {

	public static void main(String[] args) {
		
		int a = 8 ;
		
		int b = 3 ;
		
		int c = a + b ;// 8 + 3 = 11
		
		System.out.println(c);
		
		int d = a - b ; // 8 - 3 = 5
		
		System.out.println(d);
		
		int e = a * b ; // 8 * 3 = 24
		
		System.out.println(e);
		
		int f = a / b ; // 8 / 3 = 2
		
		System.out.println(f);
		
		int g = a % b ; // 8 % 3 means remainder of 8 / 3 which is 2.
		
		System.out.println(g);
	}
}
